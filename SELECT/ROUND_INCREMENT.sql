DECLARE @ROUND_INCREMENT AS int = 500

SELECT
	AMOUNT_TO_ROUND,
	@ROUND_INCREMENT AS ROUND_INCREMENT,
	CEILING(AMOUNT_TO_ROUND / @ROUND_INCREMENT) * @ROUND_INCREMENT AS ROUNDED_AMOUNT
FROM (VALUES
	(0.00),
	(0.01),
	(499.99),
	(500.00),
	(500.01),
	(1499.99),
	(1500.00),
	(1500.01)
) AS T(AMOUNT_TO_ROUND)
